class ArraySimple {
    public static void main(String[] args) {
        // On crée un tableau d'entier avec une taille de 10.
        // Dans le cas d'une liste d'entiers, chaque cellule est initialisée à 0.
        int[] tableau = new int[10];

        // On assigne 1 à la cellule ayant pour index 0.
        tableau[0] = 1;

        // On imprime la valeur de la cellule ayant pour index 0 sur la sortie standard.
        System.out.println(tableau[0]);
    }
}